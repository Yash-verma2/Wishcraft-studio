async function startScreenRecording(btnElement) {
  try {
    // 1. Prompt user with modern tab constraints to share tab
    screenStream = await navigator.mediaDevices.getDisplayMedia({
      video: {
        displaySurface: "browser",
        logicalSurface: true
      },
      selfBrowserSurface: "include", // prioritize current tab
      preferCurrentTab: true,
      audio: true
    });

    screenChunks = [];
    screenRecordingSeconds = 0;

    // 2. Setup video element to read the stream pixels
    const video = document.createElement('video');
    video.srcObject = screenStream;
    video.autoplay = true;
    video.playsInline = true;

    // Wait for stream to load metadata to get resolution
    await new Promise(r => video.onloadedmetadata = r);
    // Play video element
    video.play().catch(e => console.log(e));

    // 3. Find phone screen element (to record only the inner screen content)
    const phoneScreen = document.getElementById('phoneScreen');
    if (!phoneScreen) {
      throw new Error("Could not find mobile screen container (#phoneScreen)");
    }

    const rect = phoneScreen.getBoundingClientRect();
    if (rect.width === 0 || rect.height === 0) {
      throw new Error("Mobile screen container is hidden or occupies no space");
    }

    // 4. Create canvas to render the cropped phone area
    const canvas = document.createElement('canvas');
    // Render at 2x element size for high quality/crisp layout
    canvas.width = rect.width * 2;
    canvas.height = rect.height * 2;
    const ctx = canvas.getContext('2d');

    // 5. Build output recording stream from canvas
    const recordStream = canvas.captureStream(30);

    // Add captured audio tracks to output stream (if system/tab audio was shared)
    const audioTracks = screenStream.getAudioTracks();
    audioTracks.forEach(track => recordStream.addTrack(track));

    // 6. Setup MediaRecorder with codecs fallback dynamically
    const options = getSupportedVideoOptions();

    screenRecorder = new MediaRecorder(recordStream, options);

    screenRecorder.ondataavailable = (e) => {
      if (e.data && e.data.size > 0) {
        screenChunks.push(e.data);
      }
    };

    screenRecorder.onstop = () => {
      cleanupScreenRecording();

      if (screenChunks.length === 0) return;

      const finalMime = screenRecorder.mimeType || options.mimeType || 'video/webm';
      const blob = new Blob(screenChunks, { type: finalMime });
      const url = URL.createObjectURL(blob);
      const link = document.createElement('a');
      link.href = url;
      const isMp4 = finalMime.toLowerCase().includes('mp4') || finalMime.toLowerCase().includes('quicktime');
      const ext = isMp4 ? 'mp4' : 'webm';
      link.download = `wishcraft-mobile-layout-${Date.now()}.${ext}`;
      link.click();

      alert("Success! The recording of the mobile layout has been downloaded! 🎬");
    };

    // If the user clicks "Stop sharing" in the browser's banner
    screenStream.getVideoTracks()[0].onended = () => {
      if (screenRecorder && screenRecorder.state !== 'inactive') {
        screenRecorder.stop();
      }
    };

    // 7. Start recording loop
    isCroppedRecordingActive = true;
    function drawFrame() {
      if (!isCroppedRecordingActive) return;

      const currentRect = phoneScreen.getBoundingClientRect();
      if (currentRect.width > 0 && currentRect.height > 0) {
        // Map the CSS coordinates of the phone element in the window to the actual video stream pixels
        const scaleX = video.videoWidth / window.innerWidth;
        const scaleY = video.videoHeight / window.innerHeight;

        const cropX = currentRect.left * scaleX;
        const cropY = currentRect.top * scaleY;
        const cropW = currentRect.width * scaleX;
        const cropH = currentRect.height * scaleY;

        ctx.clearRect(0, 0, canvas.width, canvas.height);

        // Safety bounds checks to prevent drawing error if tab is minimized/offscreen
        if (cropW > 0 && cropH > 0 && cropX >= 0 && cropY >= 0 && (cropX + cropW) <= video.videoWidth && (cropY + cropH) <= video.videoHeight) {
          ctx.drawImage(video, cropX, cropY, cropW, cropH, 0, 0, canvas.width, canvas.height);
        } else {
          // Fallback if elements are shifted out of bounds (e.g. during resize/zoom)
          ctx.drawImage(video, 0, 0, video.videoWidth, video.videoHeight, 0, 0, canvas.width, canvas.height);
        }
      }
      requestAnimationFrame(drawFrame);
    }

    screenRecorder.start();
    requestAnimationFrame(drawFrame);

    updateRecordingButtonsUI(true);

    // Automatically reset and play the simulation timeline to capture the entire flow cleanly
    replayReelTimeline();

    screenRecordingInterval = setInterval(() => {
      screenRecordingSeconds++;
      const mins = String(Math.floor(screenRecordingSeconds / 60)).padStart(2, '0');
      const secs = String(screenRecordingSeconds % 60).padStart(2, '0');
      updateRecordingButtonsLabel(`⏹ Stop Recording (${mins}:${secs})`);
    }, 1000);

  } catch (err) {
    console.error("Error starting screen recording:", err);
    cleanupScreenRecording();
    alert("Recording failed. Please make sure to share the 'Current Tab' or 'This Tab' in the browser popup dialog for the crop to align correctly.");
  }
}

