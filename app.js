// Scrape OG Image and Title from url via CORS proxy
async function fetchOgImageFromLink(url) {
  try {
    let targetUrl = url.trim();
    if (!targetUrl.startsWith('http://') && !targetUrl.startsWith('https://')) {
      targetUrl = 'https://' + targetUrl;
    }

    // Use api.allorigins.win proxy
    const proxyUrl = `https://api.allorigins.win/get?url=${encodeURIComponent(targetUrl)}`;
    const response = await fetch(proxyUrl);
    if (!response.ok) return null;
    const data = await response.json();
    const htmlString = data.contents;

    const parser = new DOMParser();
    const doc = parser.parseFromString(htmlString, 'text/html');

    // Look for og:image meta tag
    let ogImageUrl = null;
    const ogMeta = doc.querySelector('meta[property="og:image"]');
    if (ogMeta) {
      ogImageUrl = ogMeta.getAttribute('content');
    } else {
      const twitterMeta = doc.querySelector('meta[name="twitter:image"]');
      if (twitterMeta) {
        ogImageUrl = twitterMeta.getAttribute('content');
      } else {
        const firstImg = doc.querySelector('img');
        if (firstImg) {
          ogImageUrl = firstImg.getAttribute('src');
        }
      }
    }

    if (ogImageUrl) {
      if (!ogImageUrl.startsWith('http://') && !ogImageUrl.startsWith('https://')) {
        const base = new URL(targetUrl);
        ogImageUrl = new URL(ogImageUrl, base.origin).href;
      }
    }

    // Look for og:title
    let ogTitle = null;
    const ogTitleMeta = doc.querySelector('meta[property="og:title"]');
    if (ogTitleMeta) {
      ogTitle = ogTitleMeta.getAttribute('content');
    } else {
      const twitterTitleMeta = doc.querySelector('meta[name="twitter:title"]');
      if (twitterTitleMeta) {
        ogTitle = twitterTitleMeta.getAttribute('content');
      } else {
        const titleTag = doc.querySelector('title');
        if (titleTag) {
          ogTitle = titleTag.textContent;
        }
      }
    }

    return {
      image: ogImageUrl,
      title: ogTitle ? ogTitle.trim() : null
    };
  } catch (e) {
    console.error("Error scraping OG image/title:", e);
  }
  return null;
}

// Scrape first link inside messages stack and update cover image
async function fetchAndSetCoverFromLink() {
  const linkBubble = activeConfig.chatBubbles.find(b => isLinkBubble(b.text));
  if (!linkBubble) {
    alert("No link found in your chat messages! Add a message containing a URL first.");
    return;
  }

  const httpMatch = linkBubble.text.match(/https?:\/\/[^\s]+/);
  let urlToFetch = httpMatch ? httpMatch[0] : '';
  if (!urlToFetch) {
    const appMatch = linkBubble.text.match(/wishcraft\.app\/[a-zA-Z0-9-_\/]+/);
    if (appMatch) {
      urlToFetch = 'https://' + appMatch[0];
    }
  }

  if (!urlToFetch) {
    alert("Could not extract a valid URL from the link message.");
    return;
  }

  fetchLinkCoverBtn.disabled = true;
  const originalHtml = fetchLinkCoverBtn.innerHTML;
  fetchLinkCoverBtn.innerHTML = `⏳ Fetching...`;

  try {
    const result = await fetchOgImageFromLink(urlToFetch);
    if (result && (result.image || result.title)) {
      const scrapedImgUrl = result.image;
      const scrapedTitle = result.title;

      const applyFetchedData = () => {
        if (scrapedTitle) {
          activeConfig.cardHeader = scrapedTitle;
          cardHeaderVal.value = scrapedTitle;
        }
        applyCardStyles();
        alert("Success! Automatically imported the preview image and title from the link! 🌟");
      };

      if (scrapedImgUrl) {
        const tempImg = new Image();
        tempImg.onload = () => {
          aiCoverArtUrl = scrapedImgUrl;
          coverArtImage = tempImg;
          applyFetchedData();
        };
        tempImg.onerror = () => {
          // Fallback without crossOrigin
          const fallbackImg = new Image();
          fallbackImg.onload = () => {
            aiCoverArtUrl = scrapedImgUrl;
            coverArtImage = fallbackImg;
            applyFetchedData();
          };
          fallbackImg.onerror = () => {
            if (scrapedTitle) {
              activeConfig.cardHeader = scrapedTitle;
              cardHeaderVal.value = scrapedTitle;
              applyCardStyles();
              alert("Success! Imported page title, but the browser could not load the image.");
            } else {
              alert("Fetched the image URL, but browser could not load the image file.");
            }
          };
          fallbackImg.src = scrapedImgUrl;
        };
        tempImg.src = scrapedImgUrl;
      } else {
        // Only title fetched
        if (scrapedTitle) {
          activeConfig.cardHeader = scrapedTitle;
          cardHeaderVal.value = scrapedTitle;
          applyCardStyles();
          alert("Success! Automatically imported the title from the link! 🌟");
        } else {
          alert("No image or title could be retrieved from this link.");
        }
      }
    } else {
      alert("We couldn't retrieve an image or title from this link. Try uploading a custom image instead!");
    }
  } catch (err) {
    console.error(err);
    alert("Error trying to scrape the link. Please upload a custom image.");
  } finally {
    fetchLinkCoverBtn.disabled = false;
    fetchLinkCoverBtn.innerHTML = originalHtml;
  }
}

