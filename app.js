// Update the chat stats indicator bar with live counts
function updateChatStats() {
  const bubbles = activeConfig.chatBubbles;
  const bubbleCount = bubbles.length;
  let totalChars = 0;
  let totalWords = 0;

  bubbles.forEach(b => {
    const text = (b.text || '').trim();
    totalChars += text.length;
    if (text.length > 0) {
      totalWords += text.split(/\s+/).filter(w => w.length > 0).length;
    }
  });

  // Average reading speed ~200 wpm for casual text; on-screen chat is read slower ~150 wpm
  // Plus ~1.5s per bubble for the animation/transition delay in the simulator
  const readSeconds = Math.ceil((totalWords / 150) * 60) + Math.ceil(bubbleCount * 1.5);

  if (statBubblesEl) statBubblesEl.innerHTML = `💬 <strong>${bubbleCount}</strong> bubble${bubbleCount !== 1 ? 's' : ''}`;
  if (statWordsEl) statWordsEl.innerHTML = `📝 <strong>${totalWords}</strong> word${totalWords !== 1 ? 's' : ''}`;
  if (statCharsEl) statCharsEl.innerHTML = `🔤 <strong>${totalChars}</strong> char${totalChars !== 1 ? 's' : ''}`;
  if (statReadTimeEl) statReadTimeEl.innerHTML = `⏱️ ~<strong>${readSeconds}s</strong> read`;

  // Add warning class if chat is getting long for a Reel
  if (chatStatsBarEl) {
    if (totalWords > 80 || bubbleCount > 8) {
      chatStatsBarEl.classList.add('stats-warning');
    } else {
      chatStatsBarEl.classList.remove('stats-warning');
    }
  }
}

