// Start dynamic particle system inside WishCraft Website mockup
function startParticleEngine() {
  clearInterval(particleSpawnInterval);
  particleContainerEl.innerHTML = '';

  const type = activeConfig.particleSelect || 'stars';
  const symbols = {
    stars: ['✨', '⭐', '💫', '✨'],
    hearts: ['💖', '❤️', '💕', '💘'],
    balloons: ['🎈', '🎈', '🎉', '🎁'],
    mixed: ['✨', '💖', '🎈', '🍀', '💎']
  };

  const pool = symbols[type] || symbols.stars;

  // Spawn 1 particle every 200ms
  particleSpawnInterval = setInterval(() => {
    if (playbackState === 'paused' || !sceneRevealEl.classList.contains('active')) return;

    const p = document.createElement('div');
    p.className = 'card-particle';

    // Pick random symbol
    p.textContent = pool[Math.floor(Math.random() * pool.length)];

    // Random position and timing properties for high fidelity randomness
    const left = Math.random() * 90 + 5; // 5% to 95%
    const size = Math.random() * 0.5 + 0.7; // 0.7rem to 1.2rem
    const duration = Math.random() * 2 + 2; // 2s to 4s
    const sway = Math.random() * 40 - 20; // -20px to 20px
    const rot = Math.random() * 360 + 90; // 90deg to 450deg

    p.style.left = `${left}%`;
    p.style.fontSize = `${size}rem`;
    p.style.animationDuration = `${duration}s`;
    p.style.setProperty('--sway', `${sway}px`);
    p.style.setProperty('--rot', `${rot}deg`);

    particleContainerEl.appendChild(p);

    // Remove after anim
    setTimeout(() => {
      p.remove();
    }, duration * 1000);
  }, 250);
}

