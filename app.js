// Utility to escape HTML strings
function escapeHtml(text) {
  return text
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#039;");
}

function isLinkBubble(text) {
  if (!text) return false;
  return text.includes('wishcraft.app') ||
    text.includes('onrender.com') ||
    text.includes('localhost:') ||
    /https?:\/\/[^\s]+/.test(text);
}

function extractCleanUrl(text) {
  if (!text) return 'wishcraft.app';
  const httpMatch = text.match(/https?:\/\/[^\s]+/);
  if (httpMatch) {
    try {
      const urlObj = new URL(httpMatch[0]);
      let displayUrl = urlObj.hostname;
      if (urlObj.pathname && urlObj.pathname !== '/') {
        let path = urlObj.pathname;
        if (path.endsWith('/')) path = path.slice(0, -1);
        displayUrl += path;
      }
      if (displayUrl.length > 32) {
        return displayUrl.substring(0, 32) + '...';
      }
      return displayUrl;
    } catch (e) {
      return 'wishcraft.app';
    }
  }
  const appMatch = text.match(/wishcraft\.app\/[a-zA-Z0-9-_\/]+/);
  if (appMatch) {
    return appMatch[0];
  }
  return 'wishcraft.app';
}

async function fetchMetadataForUrl(url) {
  let targetUrl = url.trim();
  if (!targetUrl.startsWith('http://') && !targetUrl.startsWith('https://')) {
    targetUrl = 'https://' + targetUrl;
  }

  if (linkMetadataCache[targetUrl]) return;
  linkMetadataCache[targetUrl] = { loading: true };

  try {
    const result = await fetchOgImageFromLink(targetUrl);
    if (result) {
      linkMetadataCache[targetUrl] = {
        title: result.title || null,
        imageUrl: result.image || null,
        imageObject: null
      };

      if (result.image) {
        const img = new Image();
        img.onload = () => {
          linkMetadataCache[targetUrl].imageObject = img;
          renderChatBubblesPreview();
        };
        img.src = result.image;
      } else {
        renderChatBubblesPreview();
      }
    }
  } catch (e) {
    console.error("Error background fetching metadata:", e);
    linkMetadataCache[targetUrl] = { failed: true };
  }
}

function getLinkCardTitle(bubbleText) {
  if (customLinkTitleInput && customLinkTitleInput.value.trim() !== '') {
    return customLinkTitleInput.value.trim();
  }
  if (!bubbleText) return activeConfig.cardHeader || "WishCraft • Special Message ✨";

  const httpMatch = bubbleText.match(/https?:\/\/[^\s]+/);
  let urlStr = httpMatch ? httpMatch[0] : '';
  if (!urlStr) {
    const appMatch = bubbleText.match(/wishcraft\.app\/[a-zA-Z0-9-_\/]+/);
    if (appMatch) {
      urlStr = 'https://' + appMatch[0];
    }
  }

  if (urlStr) {
    let targetUrl = urlStr.trim();
    if (!targetUrl.startsWith('http://') && !targetUrl.startsWith('https://')) {
      targetUrl = 'https://' + targetUrl;
    }

    if (linkMetadataCache[targetUrl] && linkMetadataCache[targetUrl].title) {
      return linkMetadataCache[targetUrl].title;
    }

    try {
      const urlObj = new URL(urlStr);
      let path = urlObj.pathname;
      if (path.endsWith('/')) path = path.slice(0, -1);

      if (path.startsWith('/generated/')) {
        const id = path.split('/').pop();
        return `WishCraft • Card ${id} ✨`;
      }

      if (path && path !== '/') {
        const name = path.substring(1)
          .split('/')
          .pop()
          .split('-')
          .map(word => word.charAt(0).toUpperCase() + word.slice(1))
          .join(' ');
        return `WishCraft • ${name} ✨`;
      }
    } catch (e) {
      // ignore
    }
  }
  return activeConfig.cardHeader || "WishCraft • Special Message ✨";
}

function loadRealWebsiteIframe() {
  if (!realWebsiteIframe || !realWebsiteFrameWrapper) return;

  const errorEl = document.getElementById('realWebsiteError');
  const errorUrlEl = document.getElementById('realWebsiteErrorUrl');
  const loader = document.getElementById('realWebsiteLoader');

  // Helper: show loading state
  const showLoader = () => {
    if (loader) { loader.style.display = 'flex'; loader.style.opacity = '1'; }
    if (errorEl) errorEl.style.display = 'none';
  };
  // Helper: hide loading state
  const hideLoader = () => {
    if (loader) {
      loader.style.opacity = '0';
      setTimeout(() => { loader.style.display = 'none'; }, 500);
    }
  };
  // Helper: show error state
  const showError = (url) => {
    hideLoader();
    if (errorEl) { errorEl.style.display = 'flex'; }
    if (errorUrlEl) { errorUrlEl.textContent = url || ''; }
  };

  // --- Determine target URL ---
  // Priority 1: Manually entered override URL
  let targetUrl = '';
  if (previewUrlOverrideInput && previewUrlOverrideInput.value.trim() !== '') {
    targetUrl = previewUrlOverrideInput.value.trim();
    if (!targetUrl.startsWith('http://') && !targetUrl.startsWith('https://')) {
      targetUrl = 'https://' + targetUrl;
    }
  }

  // Priority 2: Auto-detect from chat link bubble
  if (!targetUrl) {
    const linkBubble = activeConfig.chatBubbles.find(b => isLinkBubble(b.text));
    if (linkBubble) {
      const httpMatch = linkBubble.text.match(/https?:\/\/[^\s]+/);
      let urlStr = httpMatch ? httpMatch[0] : '';
      if (!urlStr) {
        const appMatch = linkBubble.text.match(/wishcraft\.app\/[a-zA-Z0-9-_\/]+/);
        if (appMatch) urlStr = 'https://' + appMatch[0];
      }
      if (urlStr) {
        targetUrl = urlStr.trim();
        if (!targetUrl.startsWith('http://') && !targetUrl.startsWith('https://')) {
          targetUrl = 'https://' + targetUrl;
        }
        // Auto-translate wishcraft.app → render domain
        if (targetUrl.includes('wishcraft.app')) {
          if (targetUrl.includes('/chloe') || targetUrl.includes('/forgive-me') ||
            targetUrl.includes('/bff-forever-chloe') || targetUrl.includes('/our-two-years')) {
            targetUrl = 'https://wishcraft-xu43.onrender.com/generated/015da2886d/';
          } else {
            targetUrl = targetUrl.replace('wishcraft.app', 'wishcraft-xu43.onrender.com');
          }
        }
      }
    }
  }

  if (!targetUrl) {
    unloadRealWebsiteIframe();
    return;
  }

  // Show frame wrapper
  realWebsiteFrameWrapper.style.display = 'block';
  showLoader();

  // Get the actual iframe element (always fresh from DOM)
  const iframeEl = document.getElementById('realWebsiteIframe');
  if (!iframeEl) return;

  // Clear any previous load timeout
  if (iframeLoadTimer) { clearTimeout(iframeLoadTimer); iframeLoadTimer = null; }

  // Listen for successful load
  iframeEl.onload = () => {
    if (iframeLoadTimer) { clearTimeout(iframeLoadTimer); iframeLoadTimer = null; }
    hideLoader();
  };

  // Timeout: if not loaded within 8s, show error fallback
  iframeLoadTimer = setTimeout(() => {
    iframeLoadTimer = null;
    showError(targetUrl);
  }, 8000);

  // Set src to trigger navigation
  if (iframeEl.src !== targetUrl) {
    iframeEl.src = targetUrl;
  } else {
    // Already loaded (cached) – hide loader immediately
    setTimeout(() => hideLoader(), 600);
  }
}
window.loadRealWebsiteIframe = loadRealWebsiteIframe;

function unloadRealWebsiteIframe() {
  if (iframeLoadTimer) { clearTimeout(iframeLoadTimer); iframeLoadTimer = null; }
  const iframeEl = document.getElementById('realWebsiteIframe');
  if (iframeEl && iframeEl.src !== 'about:blank' && iframeEl.src !== '') {
    iframeEl.onload = null;
    iframeEl.src = 'about:blank';
  }
  const errorEl = document.getElementById('realWebsiteError');
  if (errorEl) errorEl.style.display = 'none';
  if (realWebsiteFrameWrapper && realWebsiteFrameWrapper.style.display !== 'none') {
    realWebsiteFrameWrapper.style.display = 'none';
  }
}

