// Sync timeline events based on 0-100% progress
function tickTimeline(progress) {
  const totalBubbles = activeConfig.chatBubbles.length;

  // Phase 1: Chat Sequence (0% to 50% of the timeline)
  if (progress <= 50) {
    sceneChatEl.classList.add('active');
    sceneRevealEl.classList.remove('active');

    // Hide transition overlay and reset its inline styles
    transitionOverlayEl.classList.remove('active');
    transitionOverlayEl.style.opacity = '0';
    transitionOverlayEl.style.pointerEvents = 'none';
    transitionOverlayEl.style.animation = 'none';

    // Ensure real website iframe is unloaded during chatting phase
    unloadRealWebsiteIframe();

    // Distribute the chat duration evenly among all bubbles
    const stepSize = 50 / Math.max(1, totalBubbles);
    const completedCount = Math.floor(progress / stepSize);
    const segmentProgress = (progress % stepSize) / stepSize;

    let targetCompletedCount = completedCount;
    let shouldShowTypingDots = false;

    if (completedCount < totalBubbles) {
      const activeBubble = activeConfig.chatBubbles[completedCount];
      if (segmentProgress >= 0.85) {
        targetCompletedCount = completedCount + 1;
      } else if (segmentProgress >= 0.15) {
        if (activeBubble.side === 'left') {
          shouldShowTypingDots = true;
        } else {
          // Character-by-character typing in bottom input bar (sender typing)
          const totalChars = activeBubble.text.length;
          const typingSubProgress = (segmentProgress - 0.15) / 0.7; // map 0.15-0.85 to 0-1
          const charCount = Math.floor(totalChars * typingSubProgress);
          const typedText = activeBubble.text.substring(0, charCount);
          updateChatInputWithText(typedText);
        }
      } else {
        // Preparation phase - clear typing text
        resetChatInputs();
      }
    } else {
      // All bubbles completed
      resetChatInputs();
    }

    // Get current regular bubbles in the DOM (excluding typing dots indicator)
    const currentBubbles = chatFeedEl.querySelectorAll('.chat-bubble-wrap:not(#typingIndicator) > .chat-bubble');

    if (currentBubbles.length > targetCompletedCount) {
      // User rewound/dragged timeline backward - clean rebuild
      chatFeedEl.innerHTML = '';
      for (let i = 0; i < targetCompletedCount; i++) {
        appendBubbleToFeed(activeConfig.chatBubbles[i]);
      }
    } else if (currentBubbles.length < targetCompletedCount) {
      // Send new bubbles incrementally
      const indicator = document.getElementById('typingIndicator');
      if (indicator) {
        const wrap = indicator.closest('.chat-bubble-wrap');
        if (wrap) wrap.remove();
        else indicator.remove();
      }

      for (let i = currentBubbles.length; i < targetCompletedCount; i++) {
        appendBubbleToFeed(activeConfig.chatBubbles[i]);

        // Play bubble delivery sound (only play if playing preview in real time)
        if (soundEnabled && soundKeyboard && playbackState === 'playing') {
          soundKeyboard.currentTime = 0;
          soundKeyboard.play().catch(e => { });
        }
      }
    }

    // Manage Bouncing dots typing indicator state
    const indicator = document.getElementById('typingIndicator');
    if (shouldShowTypingDots) {
      if (!indicator) {
        renderBouncingDotsIndicator();
      }
      // Hide seen receipt while typing dots are active
      updateSeenReceipt(false);
    } else {
      if (indicator) {
        const wrap = indicator.closest('.chat-bubble-wrap');
        if (wrap) wrap.remove();
        else indicator.remove();
      }
      // Show seen receipt when all bubbles are delivered
      const allDone = targetCompletedCount >= totalBubbles;
      if (allDone) {
        const lastBubble = activeConfig.chatBubbles[totalBubbles - 1];
        updateSeenReceipt(lastBubble && lastBubble.side === 'right');
      }
    }
    updateCinematicZoom();
  }

  // Phase 2: Transition Overlay Phase (50% to 60% of the timeline)
  else if (progress > 50 && progress <= 60) {
    resetCinematicZoom();

    // Determine overlay opacity based on progress (linear fade in and fade out)
    let opacity = 0;
    if (progress <= 55) {
      // Fade in from 50 to 55 (0 to 1)
      opacity = (progress - 50) / 5;
    } else {
      // Fade out from 55 to 60 (1 to 0)
      opacity = (60 - progress) / 5;
    }

    // Swap the active scenes at the peak opacity (55%)
    if (progress < 55) {
      sceneChatEl.classList.add('active');
      sceneRevealEl.classList.remove('active');
      unloadRealWebsiteIframe();
    } else {
      sceneChatEl.classList.remove('active');
      if (!sceneRevealEl.classList.contains('active')) {
        sceneRevealEl.classList.add('active');
        if (soundEnabled && playbackState === 'playing') {
          soundMagic.currentTime = 0;
          soundMagic.play().catch(e => { });
        }
        loadRealWebsiteIframe();
      }
    }

    // Show overlay, setting opacity and disabling CSS animations
    transitionOverlayEl.style.pointerEvents = 'auto';
    transitionOverlayEl.style.opacity = opacity;
    transitionOverlayEl.style.animation = 'none';
    if (!transitionOverlayEl.classList.contains('active')) {
      transitionOverlayEl.classList.add('active');
      triggerTransitionOverlay();
    }

    // Direct scale adjustment for blur transition to make it feel extra premium
    const blurEl = transitionOverlayEl.querySelector('.transition-blur');
    if (blurEl) {
      blurEl.style.transition = 'none'; // Disable CSS transitions for progress-lock
      const scale = 0.8 + ((progress - 50) / 10) * 1.0;
      blurEl.style.transform = `scale(${scale})`;
    }
  }
  // Phase 3: Real Website Reveal (60% to 100% of the timeline)
  else if (progress > 60) {
    resetCinematicZoom();
    sceneChatEl.classList.remove('active');

    // Hide transition overlay and reset its inline styles
    transitionOverlayEl.classList.remove('active');
    transitionOverlayEl.style.opacity = '0';
    transitionOverlayEl.style.pointerEvents = 'none';
    transitionOverlayEl.style.animation = 'none';

    if (!sceneRevealEl.classList.contains('active')) {
      sceneRevealEl.classList.add('active');

      if (soundEnabled && playbackState === 'playing') {
        soundMagic.currentTime = 0;
        soundMagic.play().catch(e => console.log('Audio blocked', e));
      }

      // Dynamically load the real website iframe
      loadRealWebsiteIframe();
    }
  }
}


