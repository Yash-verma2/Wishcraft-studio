function togglePlayback() {
  if (playbackState === 'playing') {
    pauseTimeline();
  } else {
    playTimeline();
  }
}

function playTimeline() {
  playbackState = 'playing';
  playIconEl.textContent = '⏸';
  playPauseBtn.innerHTML = `<span>⏸</span> Pause Preview`;

  if (studioActive) {
    studioStartBtn.innerHTML = `<span>⏸</span> Pause`;
    studioStartBtn.className = 'sim-btn';
    studioPauseBtn.style.display = 'inline-flex';
  }

  // If starting fresh or looping
  if (playbackProgress >= 100 || playbackProgress === 0) {
    playbackProgress = 0;
    resetReelSimulator();
  }

  // Activate audio waveform visualizer animation
  if (audioWaveVisualizer) {
    audioWaveVisualizer.classList.add('playing');
  }

  // Play preloaded background viral audio tracks
  if (soundEnabled) {
    try {
      const selectEl = document.getElementById('studioAudioTrackSelect');
      const trackKey = selectEl ? selectEl.value : 'lofi';
      const trackUrl = viralAudioTracks[trackKey] || viralAudioTracks.lofi;

      if (!simulatedAudioPlayer) {
        simulatedAudioPlayer = new Audio(trackUrl);
        simulatedAudioPlayer.loop = true;
        simulatedAudioPlayer.volume = 0.4;
      } else if (simulatedAudioPlayer.src !== trackUrl) {
        simulatedAudioPlayer.pause();
        simulatedAudioPlayer.src = trackUrl;
        simulatedAudioPlayer.load();
      }
      simulatedAudioPlayer.play().catch(err => console.log("Audio play blocked by policy", err));
    } catch (e) {
      console.log("Audio creation error", e);
    }
  }

  // Set interval to update progress based on the dynamic speed slider value
  const tickDelay = speedSlider ? parseInt(speedSlider.value) : 200;
  playbackInterval = setInterval(() => {
    playbackProgress += 1;
    progressBarEl.style.width = `${playbackProgress}%`;

    if (playbackProgress >= 100) {
      playbackProgress = 100;
      progressBarEl.style.width = '100%';
      pauseTimeline();
      return;
    }

    tickTimeline(playbackProgress);
  }, tickDelay);
}

function pauseTimeline() {
  playbackState = 'paused';
  playIconEl.textContent = '▶';
  playPauseBtn.innerHTML = `<span>▶</span> Play`;

  resetCinematicZoom();

  if (studioActive) {
    studioStartBtn.innerHTML = `🎬 Resume Play`;
    studioStartBtn.className = 'sim-btn primary';
    studioPauseBtn.style.display = 'none';
  }

  if (playbackInterval) {
    clearInterval(playbackInterval);
  }
  clearTimeout(timelineTimer);

  // Stop audio wave visualizer
  if (audioWaveVisualizer) {
    audioWaveVisualizer.classList.remove('playing');
  }

  // Pause simulated background music track
  if (simulatedAudioPlayer) {
    simulatedAudioPlayer.pause();
  }
}

function stopTimeline() {
  pauseTimeline();
  playbackProgress = 0;
  progressBarEl.style.width = '0%';

  if (simulatedAudioPlayer) {
    simulatedAudioPlayer.currentTime = 0;
  }
}

function replayReelTimeline() {
  stopTimeline();
  playTimeline();
}

