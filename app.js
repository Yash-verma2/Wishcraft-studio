/* Cinematic Auto-Zoom and Focus Camera Engine */
function updateCinematicZoom() {
  if (!cinematicZoomEnabled || playbackState !== 'playing' || playbackProgress > 50) {
    resetCinematicZoom();
    return;
  }

  let activeEl = null;

  // 1. Focus on standard typing indicator if active
  const typingIndicator = document.getElementById('typingIndicator');
  if (typingIndicator) {
    activeEl = typingIndicator.querySelector('.chat-bubble') || typingIndicator;
  } else {
    // 2. Otherwise, focus on the most recent bubble that has appeared
    const bubbles = chatFeedEl.querySelectorAll('.chat-bubble-wrap:not(#typingIndicator) .chat-bubble');
    if (bubbles.length > 0) {
      activeEl = bubbles[bubbles.length - 1];
    }
  }

  if (!activeEl) {
    resetCinematicZoom();
    return;
  }

  // Toggle handheld animations based on style
  const viewport = document.getElementById('chatFeedViewport');
  if (viewport) {
    if (cinematicCameraStyle === 'handheld') {
      viewport.classList.add('handheld-heavy');
      viewport.classList.remove('cinematic-active');
    } else {
      viewport.classList.add('cinematic-active');
      viewport.classList.remove('handheld-heavy');
    }
  }

  // Handle transition class overrides on chatFeedEl
  if (chatFeedEl) {
    if (cinematicCameraStyle === 'punch') {
      chatFeedEl.classList.add('transition-punch');
      chatFeedEl.classList.remove('transition-thirds');
    } else if (cinematicCameraStyle === 'thirds') {
      chatFeedEl.classList.add('transition-thirds');
      chatFeedEl.classList.remove('transition-punch');
    } else {
      chatFeedEl.classList.remove('transition-punch', 'transition-thirds');
    }
  }

  // Cinematic Depth-of-Field bokeh: Focus current bubble, blur and fade others
  const allElements = chatFeedEl.querySelectorAll('.chat-bubble, .bubble-timestamp');
  allElements.forEach(el => {
    if (el === activeEl) {
      el.classList.add('focus-in');
      el.classList.remove('blur-out');
    } else {
      el.classList.add('blur-out');
      el.classList.remove('focus-in');
    }
  });

  // Calculate coordinates to center camera
  const feedRect = chatFeedEl.getBoundingClientRect();
  const elRect = activeEl.getBoundingClientRect();

  if (feedRect.height === 0 || elRect.height === 0) return;

  const feedCenterY = feedRect.top + feedRect.height / 2;
  const elCenterY = elRect.top + elRect.height / 2;
  const feedCenterX = feedRect.left + feedRect.width / 2;
  const elCenterX = elRect.left + elRect.width / 2;

  // Camera Style Parameters
  let scale = 1.35;
  let translateXMultiplier = 0.25;
  let translateXOffset = 0;
  let recoilForce = 0.06;
  let recoilDuration = 350;

  if (cinematicCameraStyle === 'punch') {
    scale = 1.42;
    recoilForce = 0.12; // Extra heavy snappiness
    recoilDuration = 280; // Fast decay
  } else if (cinematicCameraStyle === 'thirds') {
    scale = 1.28;
    translateXMultiplier = 0.8; // Slide more horizontally
    recoilForce = 0.04;
    recoilDuration = 450;
  } else if (cinematicCameraStyle === 'handheld') {
    scale = 1.25; // Slower zoom, heavy float focus
    recoilForce = 0.03;
    recoilDuration = 400;
  }

  // Adjust scale based on message text length (to prevent clipping)
  if (activeEl.id !== 'typingIndicator' && !activeEl.closest('#typingIndicator')) {
    const textLength = activeEl.textContent ? activeEl.textContent.trim().length : 0;
    if (textLength > 60) {
      scale -= 0.15; // Zoom out slightly for longer text blocks
    } else if (textLength > 30) {
      scale -= 0.08;
    }
  } else {
    // Focus closer on typing dots to build tension
    scale += 0.08;
  }

  // Recoil/Bounce Impact effect when focus shifts to a new bubble
  if (window.lastActiveEl !== activeEl) {
    window.lastActiveEl = activeEl;
    window.activeElChangeTime = performance.now();
  }

  const msSinceChange = performance.now() - (window.activeElChangeTime || 0);
  if (msSinceChange < recoilDuration) {
    const punchProgress = 1 - (msSinceChange / recoilDuration); // 1 to 0
    scale += recoilForce * Math.sin(punchProgress * Math.PI);
  }

  const translateY = (feedCenterY - elCenterY);

  // Calculate horizontal shift (Rule of Thirds vs Centered)
  let translateX = (feedCenterX - elCenterX) * translateXMultiplier;
  if (cinematicCameraStyle === 'thirds') {
    // Offset left-aligned bubbles to the left-third, right-aligned to the right-third
    const isLeft = activeEl.classList.contains('left');
    translateX += isLeft ? -45 : 45;
  }

  chatFeedEl.style.transformOrigin = 'center center';
  chatFeedEl.style.transform = `scale(${scale}) translate(${translateX}px, ${translateY}px)`;
}

function resetCinematicZoom() {
  // Disable handheld camera animations
  const viewport = document.getElementById('chatFeedViewport');
  if (viewport) {
    viewport.classList.remove('cinematic-active', 'handheld-heavy');
  }

  // Clear transition classes
  if (chatFeedEl) {
    chatFeedEl.classList.remove('transition-punch', 'transition-thirds');
    const elements = chatFeedEl.querySelectorAll('.chat-bubble, .bubble-timestamp');
    elements.forEach(el => {
      el.classList.remove('focus-in', 'blur-out');
    });
    chatFeedEl.style.transform = 'scale(1) translate(0, 0)';
  }
}

